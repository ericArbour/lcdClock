function lcd (num) {
 var strings = [
  [[' ', '_', ' '], ['|', ' ', '|'], ['|', '_', '|']],
  [[' ', ' ', ' '], [' ', ' ', '|'], [' ', ' ', '|']],
  [[' ', '_', ' '], [' ', '_', '|'], ['|', '_', ' ']],
  [[' ', '_', ' '], [' ', '_', '|'], [' ', '_', '|']],
  [[' ', ' ', ' '], ['|', '_', '|'], [' ', ' ', '|']],
  [[' ', '_', ' '], ['|', '_', ' '], [' ', '_', '|']],
  [[' ', '_', ' '], ['|', '_', ' '], ['|', '_', '|']],
  [['_', '_', ' '], [' ', '/', ' '], ['/', ' ', ' ']],
  [[' ', '_', ' '], ['|', '_', '|'], ['|', '_', '|']],
  [[' ', '_', ' '], ['|', '_', '|'], [' ', ' ', '|']]
 ];
 var numString = num.toString();
 var result = '';
 for (var i = 0; i < numString.length; i++) {
  var line = [];
  for (var j = 0; j < numString.length; j++) {
   line = [line.concat(' ',  strings[Number(numString[j])][i]).join('')];
  }
  result += line + '\n';
 }
 return result;
}
console.log(lcd(1234567890));
